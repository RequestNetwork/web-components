import {
  CurrencyManager,
  isERC20Currency,
  isNativeCurrency,
} from "@requestnetwork/currency";
import type { RequestLogicTypes } from "@requestnetwork/types";

export const getSupportedCurrencies = () => {
  return CurrencyManager.getDefaultList().filter(
    (currency) => isNativeCurrency(currency) || isERC20Currency(currency)
  );
};

type CurrencyValue = RequestLogicTypes.ICurrency & { value: string };

export const CURRENCIES: Record<string, CurrencyValue> =
  getSupportedCurrencies().reduce(
    (acc, currency) => {
      const key = `${currency.symbol}_${
        "network" in currency ? currency.network : "UNKNOWN"
      }`.toUpperCase();
      acc[key] = { ...currency, value: "0" };
      return acc;
    },
    {} as Record<string, CurrencyValue>
  );

